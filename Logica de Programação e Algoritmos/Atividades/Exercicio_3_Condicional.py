numero = int(input("Digite um numero: "))

if(numero == 1):
    print(" É Domingo!")
elif(numero == 2):
    print("É Segunda!")
elif(numero == 3):
    print("É Terça!")
elif(numero == 4):
    print("É Quarta!")
elif(numero == 5):
    print("É Quinta!")
elif(numero == 6):
    print("É Sexta!")
elif(numero == 7):
    print("É Sábado!")
else:
    print("Dia inválido!")
